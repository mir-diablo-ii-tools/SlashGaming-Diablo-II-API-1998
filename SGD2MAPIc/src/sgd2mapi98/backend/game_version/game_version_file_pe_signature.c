enum {
  kSortedVersionTableCount = sizeof(kSortedVersionTable)
      / sizeof(kSortedVersionTable[0]),
};

static enum D2_GameVersion SearchTable(
    const struct Mapi_FilePeSignature* file_pe_signature
) {
  const struct VersionTableEntry* search_result;

  /*
   * This use of file_pe_signature as a VersionTableEntry is safe,
   * because only the key is ever accessed by the compare function
   * and the key matches the type of file_pe_signature.
   */
  search_result = bsearch(
      file_pe_signature,
      kSortedVersionTable,
      kSortedVersionTableCount,
      sizeof(kSortedVersionTable[0]),
      &VersionTableEntry_CompareKeyAsVoid
  );

  if (search_result == NULL) {
    Mdc_Error_ExitOnGeneralError(
        L"Error",
        L"Could not determine the game version from the file's PE "
            L"signature.",
        __FILEW__,
        __LINE__
    );

    return -1;
  }

  return search_result->value;
}

/**
 * External
 */

enum D2_GameVersion FilePeSignature_GuessGameVersion(
    int is_game_version_at_least_1_14
) {
  const wchar_t* path;
  struct Mapi_FilePeSignature file_pe_signature;

  path = is_game_version_at_least_1_14
      ? Mapi_GameExecutable_GetPath()
      : L"Storm.dll";

  Mapi_FilePeSignature_ReadFile(&file_pe_signature, path, kSignatureCount);

  return SearchTable(&file_pe_signature);
}

int FilePeSignature_HasCheck(enum D2_GameVersion game_version) {
  switch (game_version) {
    case D2_GameVersion_kBeta1_02:
    case D2_GameVersion_kBeta1_02StressTest:
    case D2_GameVersion_k1_00:
    case D2_GameVersion_k1_01:
    case D2_GameVersion_k1_06:
    case D2_GameVersion_k1_06B:
    case D2_GameVersion_k1_07Beta:
    case D2_GameVersion_k1_07:
    case D2_GameVersion_kClassic1_14A:
    case D2_GameVersion_kLod1_14A:
    case D2_GameVersion_kClassic1_14B:
    case D2_GameVersion_kLod1_14B:
    case D2_GameVersion_kClassic1_14C:
    case D2_GameVersion_kLod1_14C:
    case D2_GameVersion_kClassic1_14D:
    case D2_GameVersion_kLod1_14D: {
      return 1;
    }

    default: {
      return 0;
    }
  }
}
